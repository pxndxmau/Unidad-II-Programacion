def triangle_area(b, h: float) -> float:
    return (b * h) / 2


'''Se define la formula del area del triangulo'''


def triangle_perimeter(a: float, b: float, c: float) -> float:
    return a + b + c


'''Se define la formula de perimetro del triangulo'''
